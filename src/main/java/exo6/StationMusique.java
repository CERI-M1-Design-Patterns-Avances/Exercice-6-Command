public class StationMusique {
	
	public void marche () {
		System.out.println("La musique se met à jouer");
	}
	
	public void arreter () {
		System.out.println("La musique est arrétée");
	}
	
	public void suspendre() {
		System.out.println("La musique est suspendue");
	}
		
	public void pisteSuivante () {
		System.out.println("On passe au morceau suivant");
	}
	
	public void debutPiste () {
		System.out.println("On reprend au début du morceau");
	}
}
